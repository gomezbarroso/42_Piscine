int	ft_str_is_printable(char *str)
{
	int	i;

	i = 0;
	while (*str != '\0')
	{
		if (*str >= 32 && *str <= 126)
		{
			i = 1;
		}
		else
		{
			i = 0;
			break ;
		}
		str ++;
	}
	if (*str == '\0')
	{
		i = 1;
	}
	return (i);
}
